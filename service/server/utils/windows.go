func GetCmdOutput(cmd *exec.Cmd, isCombine bool) (string, error) {
	// windows不弹出黑色命令行窗口
	if runtime.GOOS == "windows" {
		cmd.SysProcAttr = &syscall.SysProcAttr{HideWindow: true}
	}
	var output []byte
	var err error
	if isCombine {
		output, err = cmd.CombinedOutput()
	} else {
		output, err = cmd.Output()
	}
	if err != nil {
		fmt.Println("执行cmd命令失败:", err)
		return "", err
	}

	return strings.TrimSpace(string(output)), nil
}

func OpenBrowser(url string) error {
	var cmd string
	var args []string

	switch runtime.GOOS {
	case "windows":
		cmd = "cmd"
		args = []string{"/c", "start"}
	case "darwin":
		cmd = "open"
	default: // Linux 等其他 Unix-like 系统
		// cmd = "xdg-open"
		cmd = ""
	}
	if cmd == "" {
		return nil
	}

	args = append(args, url)
	ec := exec.Command(cmd, args...)
	if runtime.GOOS == "windows" {
		ec.SysProcAttr = &syscall.SysProcAttr{HideWindow: true}
	}
	return ec.Start()
}

func GenTaskBarIcon() {
	if runtime.GOOS == "windows" {
		systray.Run(onReady, onExit)
	}
}

func onReady() {
	systray.SetIcon(iconData)
	systray.SetTitle("D-Monitor")
	systray.SetTooltip("D-Monitor 右键点击打开菜单！")
	menuOpen := systray.AddMenuItem("打开网页", "打开系统网页")
	systray.AddSeparator()
	menuQuit := systray.AddMenuItem("退出", "退出程序")

	go func() {
		for {
			select {
			case <-menuOpen.ClickedCh:
				OpenBrowser(fmt.Sprintf("http://localhost:%d/", base.RunPort))
			case <-menuQuit.ClickedCh:
				systray.Quit()
				os.Exit(0)
			}
		}
	}()

}

func onExit() {}

